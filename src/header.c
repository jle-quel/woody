#include <woody.h>

////////////////////////////////////////////////////////////////////////////////
/// PUBLIC FUNCTION
////////////////////////////////////////////////////////////////////////////////

void modify_header(t_elf *elf)
{
	Elf64_Ehdr *header = NULL;
	
	header = (Elf64_Ehdr *)elf->ptr;

	header->e_shoff += PAGE_SIZE;
	elf->old_entry = header->e_entry;
	header->e_entry = elf->new_entry;
}
